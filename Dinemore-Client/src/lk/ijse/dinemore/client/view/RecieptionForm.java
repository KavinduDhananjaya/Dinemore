    private void txtOrderIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtOrderIDActionPerformed
        try {
            OrderDTO searchCustomer = OrderController.searchOrder(txtOrderID.getText());
            if (searchCustomer != null) {
                reservedCustomerID = txtOrderID.getText();
                boolean reserveCustomer = OrderController.reserveCustomer(reservedCustomerID);
                if (reserveCustomer) {
                    txtOrderID.setText(searchCustomer.getOrderId());
                    txtCustomerName.setText(searchCustomer.getCustomerName());
                    txtTel.setText(searchCustomer.getTel());
                    cmbMenu.getAccessibleContext().toString();
//                    txtMenuID1.setText(searchCustomer.getMenuId());
                    txtTime.setText(searchCustomer.getTime());

                } else {
                    JOptionPane.showMessageDialog(this,
                            reservedCustomerID + "Customer has been Already Reserved");
                    clearText();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Not Found Customer");
            }
        } catch (Exception ex) {
            Logger.getLogger(RecieptionForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtOrderIDActionPerformed

