    private void loadAllOrders() {
        DefaultTableModel dtm = (DefaultTableModel) tbl1.getModel();
        dtm.setRowCount(0);
        try {
            List<OrderDTO> orders = OrderController.getAllOrders();
            if (orders == null) {
                return;
            }

            for (OrderDTO order : orders) {
                Object[] rowData = {
                    order.getOrderId(),
                    order.getMenuId(),
                    order.getQty(),
                    order.getCustomerName(),
                    order.getTel(),
                    order.getTime(),
                    order.getStatus()
                };
                dtm.addRow(rowData);
            }

        } catch (Exception ex) {
            Logger.getLogger(RecieptionForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

